# In this case, since the variables are assigned numerical values they are added together first and then displayed.
# However, if we enclose the numbers within quotations then they are treated as Strings and are displayed as is. It is demonstrated below:
varA = "2"
varB = "3"
print(f" {varA}+{varB} = {varA+varB}")
